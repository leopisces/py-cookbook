"""
第一个Python程序 - Hello World

学习目标：
  - print() 函数的多种用法
  - 多行输出与中文输出
  - 基本转义字符
"""

def main():
    # ========== 1. 第一个程序：打印一句话 ==========
    print("=== 1. 基本输出 ===")
    print("Hello, World!")               # 打印英文
    print("你好，世界！")                 # 打印中文

    # ========== 2. 打印多个内容 ==========
    print("\n=== 2. 打印多个内容 ===")
    # print() 可以同时打印多个值，默认用空格分隔
    print("Python", "版本:", 3.10, "+")
    # 使用 sep 参数自定义分隔符
    print("Python", "基础", "教程", sep=" -> ")
    # 使用 end 参数自定义行尾（默认是换行）
    print("第一句", end=", ")
    print("第二句", end=" — ")
    print("结尾")

    # ========== 3. 多行输出 ==========
    print("\n=== 3. 多行输出 ===")
    # 方式一：多个 print()
    print("第一行")
    print("第二行")
    print("第三行")

    # 方式二：三引号字符串（三个单引号或双引号均可）
    poem = """静夜思
床前明月光，
疑是地上霜。
举头望明月，
低头思故乡。"""
    print(poem)

    # 方式三：使用转义字符 \n
    print("第一行\n第二行\n第三行")

    # ========== 4. 转义字符 ==========
    print("\n=== 4. 转义字符 ===")
    print("Tab 制表符:\t缩进内容")         # \t 制表符
    print("包含\"双引号\"的字符串")          # \" 转义双引号
    print("包含\\反斜杠\\的路径")            # \\ 转义反斜杠
    print("第一行\r覆盖开头")               # \r 回车（将光标移到行首）

    # ========== 5. f-string 格式化输出 ==========
    print("\n=== 5. f-string 格式化输出 ===")
    name = "小明"
    age = 20
    score = 95.5
    # f-string 是 Python 3.6+ 的便捷格式化方式
    print(f"姓名: {name}")
    print(f"年龄: {age}")
    print(f"成绩: {score}")
    print(f"{name}今年{age}岁，考了{score}分")


if __name__ == "__main__":
    main()
