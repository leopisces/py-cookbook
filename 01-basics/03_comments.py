"""
Python 注释

学习目标：
  - 单行注释（#）
  - 多行注释（三引号）
  - 中文注释与编码声明
  - 注释的最佳实践
"""

def main():
    # ========== 1. 单行注释 ==========
    print("=== 1. 单行注释 ===")

    # 这是一个单行注释，以 # 开头
    name = "Python"  # 行末注释：解释这行代码的作用
    print(f"语言名称: {name}")

    # 注释可以用来临时禁用代码
    # print("这行代码不会执行")
    print("只有这行会被执行")

    # ========== 2. 多行注释 ==========
    print("\n=== 2. 多行注释 ===")

    # 方式一：使用三个双引号（推荐用于文档字符串）
    """
    这是一个多行注释。
    可以写很多行说明。
    常用于函数、类的文档说明。
    """
    print("三个双引号的多行注释")

    # 方式二：使用三个单引号
    '''
    这也是一个多行注释。
    功能与三个双引号相同。
    通常作为函数文档字符串（docstring）。
    '''
    print("三个单引号的多行注释")

    # 方式三：多个 # 连续使用（最常用的临时注释方式）
    # 第一行注释
    # 第二行注释
    # 第三行注释
    print("多个 # 号的多行注释")

    # ========== 3. 中文注释 ==========
    print("\n=== 3. 中文注释 ===")

    # Python 3 默认使用 UTF-8 编码，中文注释完全没问题
    greeting = "你好"  # 中文字符串
    print(f"中文变量: {greeting}")

    # 中文变量名也是合法的（但不推荐，可能降低代码可移植性）
    分数 = 100
    print(f"中文变量名: {分数}")

    # ========== 4. 文档字符串 (docstring) ==========
    print("\n=== 4. 文档字符串 (docstring) ===")

    def calculate_area(width, height):
        """
        计算长方形面积。

        参数:
            width (float): 宽度
            height (float): 高度

        返回:
            float: 面积 = 宽度 × 高度
        """
        return width * height

    area = calculate_area(10, 5)
    print(f"面积: {area}")

    # 可以通过 __doc__ 属性查看文档字符串
    print(f"函数文档:\n{calculate_area.__doc__}")

    # ========== 5. 注释最佳实践 ==========
    print("=== 5. 注释最佳实践 ===")

    # 好的注释：解释"为什么"而不是"是什么"
    # 使用国际标准单位（米制）进行计算
    length_m = 1.75  # 身高（米）
    weight_kg = 70   # 体重（千克）
    bmi = weight_kg / (length_m ** 2)
    print(f"BMI指数: {bmi:.2f}")

    # Python 之禅：代码应具有可读性
    # 好的代码本身就是文档，注释作为补充说明
    print("注释让代码更易于理解和维护")


if __name__ == "__main__":
    main()
